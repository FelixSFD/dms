package com.dude.dms.updater;

public class Asset {

    private String name;

    private String url;

    public Asset() {
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
